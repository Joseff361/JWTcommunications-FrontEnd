export class ClientClaim{
    id: string;
    claimId: string;
    claimState: string;
    claimReply: string;
    description: string;
    type: string;
    name: string;
    date: string;

    constructor(){
        this.id = this.id;
        this.claimId = this.claimId;
        this.claimReply = this.claimReply;
        this.claimState = this.claimState;
        this.description = this.description;
        this.type = this.type;
        this.name = this.name;
        this.date = this.date;
    }
}